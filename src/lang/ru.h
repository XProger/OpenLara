inline bool isCyrillic(char c) {
    return (c >= 'А' && c <= 'Я') || (c >= 'а' && c <= 'я');
}

inline char remapCyrillic(char c) {
    if (!isCyrillic(c)) {
        return c;
    }

    switch (c) {
        case 'а' : return 'a';
        case 'е' : return 'e';
        case 'ё' : return 'e';
        case 'и' : return 'u';
        case 'й' : return 'u';
        case 'о' : return 'o';
        case 'р' : return 'p';
        case 'с' : return 'c';
        case 'у' : return 'y';
        case 'х' : return 'x';
        case 'А' : return 'A';
        case 'В' : return 'B';
        case 'Е' : return 'E';
        case 'Ё' : return 'E';
        case 'К' : return 'K';
        case 'М' : return 'M';
        case 'Н' : return 'H';
        case 'О' : return 'O';
        case 'Р' : return 'P';
        case 'С' : return 'C';
        case 'Т' : return 'T';
        case 'Х' : return 'X';
    }
    return c;
}

